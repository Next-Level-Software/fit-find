Color getStatusColor(String? status) {
  if (status?.contains('approved') ?? false) {
    return AppColors.success;
  }

  switch (status) {
    case 'pending':
      return AppColors.darSkyBlue;
    case 'in-progress':
      return Colors.blue;
    case 'accepted':
      return AppColors.success;
    case 'active':
      return Colors.cyan;
    case 'delivered':
      return AppColors.greenGoogle;
    case 'in-queue':
      return Colors.deepOrange;
    case 'in-review':
      return AppColors.warning;
    case 'in-revision':
      return AppColors.warning;
    case 'refunded':
      return Colors.indigo;
    case 'revision':
      return AppColors.warning;
    case 'disputed':
      return AppColors.warning;
    case 'declined':
      return AppColors.danger;
    case 'rejected':
      return AppColors.danger;
    case 'cancelled':
      return AppColors.danger;
    case 'completed':
      return AppColors.success;
    case 'resolved':
      return AppColors.success;
    default:
      return Get.textTheme.bodyLarge?.color ?? Colors.black;
  }
}

Gradient customGradient({
  List<Color>? colors,
}) {
  return LinearGradient(
    colors: colors ??
        [
          const Color(0xFF5585E5),
          const Color(0xFF365696),
        ],
    begin: Alignment.centerLeft,
    end: Alignment.centerRight,
  );
}
